package reviewSessions.week3;

public class LogicicalOperators {

	public static void main(String[] args) {
		
		int apples = 10;
		int oranges = 20;
		
		if(apples == 10 && oranges == 20) {
			
			System.out.println("Condotion was true");
			
		}else {
			
			System.out.println("Condition was false");
		}

		if(apples > 10 && oranges == 20) {
			
			System.out.println("Condotion was true");
			
		}else {
			
			System.out.println("Condition was false");
		}
		
	}

}
