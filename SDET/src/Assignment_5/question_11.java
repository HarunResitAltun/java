package Assignment_5;

public class question_11 {

	public static void main(String[] args) {
		
		
		for (int h = 4; h > 0; h--) {
			
			for (int r = h; r < 4; r++) {
			
				System.out.print(" ");
			
			}
			
			for(int a = h; a > 0; a--) {
			
				System.out.print(a + " ");
			
			}
			
			System.out.print("\n");
		}
		
	}

}
